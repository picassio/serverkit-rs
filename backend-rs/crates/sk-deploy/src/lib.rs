fn id() -> String {
    Uuid::new_v4().to_string()
}
fn now() -> String {
    Utc::now().to_rfc3339()
}
fn tok(n: usize) -> String {
    rand::thread_rng()
        .sample_iter(&Alphanumeric)
        .take(n)
        .map(char::from)
        .collect()
}
fn s<'a>(b: &'a Value, k: &str, d: &'a str) -> &'a str {
    b.get(k).and_then(Value::as_str).unwrap_or(d)
}
fn opt<'a>(b: &'a Value, k: &str) -> Option<&'a str> {
    b.get(k).and_then(Value::as_str)
}
fn j(x: Option<String>) -> Value {
    x.and_then(|s| serde_json::from_str(&s).ok())
        .unwrap_or(Value::Null)
}

pub async fn ensure_schema(pool: &SqlitePool) -> anyhow::Result<()> {
    sqlx::query(r#"
CREATE TABLE IF NOT EXISTS sk_source_configs(provider TEXT PRIMARY KEY, config_encrypted TEXT NOT NULL, connected INTEGER NOT NULL DEFAULT 0, updated_at TEXT NOT NULL);
CREATE TABLE IF NOT EXISTS sk_registry_connections(id TEXT PRIMARY KEY, name TEXT NOT NULL, registry_url TEXT NOT NULL, username TEXT, secret_encrypted TEXT, created_at TEXT NOT NULL, updated_at TEXT NOT NULL);
CREATE TABLE IF NOT EXISTS sk_deploy_configs(app_id TEXT PRIMARY KEY, repo_url TEXT NOT NULL, branch TEXT NOT NULL, auto_deploy INTEGER NOT NULL DEFAULT 1, pre_deploy_script TEXT, post_deploy_script TEXT, created_at TEXT NOT NULL, updated_at TEXT NOT NULL);
CREATE TABLE IF NOT EXISTS sk_build_configs(app_id TEXT PRIMARY KEY, config_json TEXT NOT NULL, created_at TEXT NOT NULL, updated_at TEXT NOT NULL);
CREATE TABLE IF NOT EXISTS sk_deployments(id TEXT PRIMARY KEY, app_id TEXT NOT NULL, kind TEXT NOT NULL, status TEXT NOT NULL, version TEXT, config_json TEXT NOT NULL, logs TEXT, created_at TEXT NOT NULL, updated_at TEXT NOT NULL);
CREATE TABLE IF NOT EXISTS sk_deployment_logs(id TEXT PRIMARY KEY, deployment_id TEXT NOT NULL, line TEXT NOT NULL, created_at TEXT NOT NULL);
CREATE TABLE IF NOT EXISTS sk_migrations(id TEXT PRIMARY KEY, kind TEXT NOT NULL, status TEXT NOT NULL, detail_json TEXT NOT NULL, created_at TEXT NOT NULL);
"#).execute(pool).await.context("ensure sk-deploy schema")?;
    Ok(())
}

