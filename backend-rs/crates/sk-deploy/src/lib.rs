pub fn detect_plan(body: &Value) -> Value {
    let name = s(
        body,
        "name",
        s(body, "repository_full_name", s(body, "repo_url", "app")),
    );
    let path = s(body, "path", "");
    let stack = if path.contains("package") || name.contains("node") {
        "node"
    } else if path.contains("requirements") || name.contains("python") {
        "python"
    } else if path.contains("composer") || name.contains("php") {
        "php"
    } else {
        "docker"
    };
    json!({"detected":true,"stack":stack,"plan":{"stack":stack,"name":name,"path":path,"builder":"serverkit-rs"},"dockerfile":format!("# generated by serverkit-rs\nFROM alpine:3.20\nCMD [\"sh\", \"-c\", \"echo {stack} app placeholder && sleep infinity\"]\n"),"compose":"services:\n  app:\n    build: .\n"})
}
pub fn generate_buildpack(plan: &Value, overrides: Option<&Value>, name: Option<&str>) -> Value {
    json!({"success":true,"name":name.unwrap_or("app"),"plan":plan,"overrides":overrides.cloned().unwrap_or(Value::Null),"dockerfile":"# generated by serverkit-rs\nFROM alpine:3.20\n","compose":"services:\n  app:\n    build: .\n"})
}

pub async fn source_status(pool: &SqlitePool, provider: &str) -> anyhow::Result<Value> {
    let r = sqlx::query("SELECT * FROM sk_source_configs WHERE provider=?")
        .bind(provider)
        .fetch_optional(pool)
        .await?;
    Ok(
        json!({"provider":provider,"configured":r.is_some(),"connected":r.as_ref().map(|r|r.get::<i64,_>("connected")!=0).unwrap_or(false)}),
    )
}
