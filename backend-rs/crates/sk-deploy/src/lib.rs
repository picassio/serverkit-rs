    sqlx::query("UPDATE sk_registry_connections SET name=COALESCE(?,name), registry_url=COALESCE(?,registry_url), username=COALESCE(?,username), secret_encrypted=COALESCE(?,secret_encrypted), updated_at=? WHERE id=?").bind(opt(b,"name")).bind(opt(b,"registry_url").or_else(||opt(b,"url"))).bind(opt(b,"username")).bind(opt(b,"password").map(sk_core::crypto::encrypt)).bind(now()).bind(id).execute(pool).await?;
