    let id = id();
    let ts = now();
    sqlx::query("INSERT INTO sk_registry_connections(id,name,registry_url,username,secret_encrypted,created_at,updated_at) VALUES(?,?,?,?,?,?,?)").bind(&id).bind(s(b,"name","Registry")).bind(s(b,"registry_url",s(b,"url",""))).bind(opt(b,"username")).bind(opt(b,"password").map(sk_core::crypto::encrypt)).bind(&ts).bind(&ts).execute(pool).await?;
