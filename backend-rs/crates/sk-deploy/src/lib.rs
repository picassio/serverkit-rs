pub async fn put_source_config(
    pool: &SqlitePool,
    provider: &str,
    body: &Value,
) -> anyhow::Result<Value> {
    sqlx::query("INSERT INTO sk_source_configs(provider,config_encrypted,connected,updated_at) VALUES(?,?,?,?) ON CONFLICT(provider) DO UPDATE SET config_encrypted=excluded.config_encrypted, connected=excluded.connected, updated_at=excluded.updated_at").bind(provider).bind(sk_core::crypto::encrypt(&body.to_string())).bind(if body.get("token").is_some()||body.get("access_token").is_some(){1}else{0}).bind(now()).execute(pool).await?;
    source_status(pool, provider).await
}
