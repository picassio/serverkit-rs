pub async fn create_deployment(
    pool: &SqlitePool,
    app_id: &str,
    kind: &str,
    b: &Value,
) -> anyhow::Result<Value> {
    let id = id();
    let ts = now();
    let version = format!("{}-{}", kind, &id[..8]);
    sqlx::query("INSERT INTO sk_deployments(id,app_id,kind,status,version,config_json,logs,created_at,updated_at) VALUES(?,?,?,?,?,?,?,?,?)").bind(&id).bind(app_id).bind(kind).bind("completed").bind(&version).bind(b.to_string()).bind(format!("{kind} recorded by serverkit-rs")).bind(&ts).bind(&ts).execute(pool).await?;
    let _ = sk_jobs::insert_job(
        pool,
        &format!("{kind}.deployment"),
        json!({"deployment_id":id,"app_id":app_id}),
    )
    .await;
    deployment_detail(pool, &id)
        .await?
        .context("created deployment missing")
}
