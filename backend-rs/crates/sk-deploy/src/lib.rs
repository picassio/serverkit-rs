#[cfg(test)]
mod tests {
    use super::*;
    #[tokio::test]
    async fn deploy_record_roundtrip() {
        let p = SqlitePool::connect("sqlite::memory:").await.unwrap();
        ensure_schema(&p).await.unwrap();
        let d = create_deployment(&p, "app1", "build", &json!({}))
            .await
            .unwrap();
        assert_eq!(d["app_id"], "app1");
        assert_eq!(
            deployment_jobs(&p).await.unwrap()["jobs"]
                .as_array()
                .unwrap()
                .len(),
            1
        );
    }
}
